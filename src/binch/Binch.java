package binch;

public class Binch {
	int bicode;
	String Maker;
	String BinchName;
	String Exp;
	String Price;
	String Producer;
	String imgfname;
	
	
	
	public String getImgfname() {
		return imgfname;
	}
	public void setImgfname(String imgfname) {
		this.imgfname = imgfname;
	}
	public int getBicode() {
		return bicode;
	}
	public void setBicode(int bicode) {
		this.bicode = bicode;
	}
	public String getMaker() {
		return Maker;
	}
	public void setMaker(String maker) {
		Maker = maker;
	}
	public String getBinchName() {
		return BinchName;
	}
	public void setBinchName(String binchName) {
		BinchName = binchName;
	}
	public String getExp() {
		return Exp;
	}
	public void setExp(String exp) {
		Exp = exp;
	}
	public String getPrice() {
		return Price;
	}
	public void setPrice(String price) {
		Price = price;
	}
	public String getProducer() {
		return Producer;
	}
	public void setProducer(String producer) {
		Producer = producer;
	}
	
	

}
